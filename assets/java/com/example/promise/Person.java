public class Person {

    private String name;
    private String userID;
    private String email;
    private String phone;
    private String password;
    private String checkPass;
    private String loginID;
    private String loginPass;



    public String getLoginID() {return loginID;}

    public void setLoginID(String loginID) {this.loginID = loginID;}

    public String getLoginPass() {return loginPass;}

    public void setLoginPass(String loginPass) {this.loginPass = loginPass;}

    public String getName() {return name;}

    public void setName(String name) {this.name = name;}


    public String getUserID() {return userID;}

    public void setUserID(String userID) {this.userID = userID;}


    public String getEmail() {return email;}

    public void setEmail(String email) {this.email = email;}


    public String getPhone() {return phone;}

    public void setPhone(String phone) {this.phone = phone;}


    public String getPassword() {return password;}

    public void setPassword(String password) {this.password = password;}


    public String getCheckPass() {return checkPass;}

    public void setCheckPass(String checkPass) {this.checkPass = checkPass;}

}

